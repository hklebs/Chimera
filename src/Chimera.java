    // Decides which animal is next in rotation
    // This should probably change from nested switch statements and use 
    // a doubly linked list or something
    private Animal cycleAnimals(Animal current, String direction, String part){
        
        Animal result = goldenRetriever;
        
        if (direction.equals("Forwards")){
            switch (current.name){
                case "Dog":
                    result = calico;
                    break;
                case "Cat":
                    result = rabbit;
                    break;
                case "Rabbit":
                    result = elephant;
                    break;
                case "Elephant":
                    result = brownBear;
                    break;
                case "Bear":
                    if(part.equals("head"))
                        result = dogBreedHead;
                    else if(part.equals("torso"))
                        result = dogBreedTorso;
                    else
                        result = dogBreedLegs;
                    break;
            }
        } else if (direction.equals("Backwards")){
            switch (current.name){
                case "Dog":
                    result = brownBear;
                    break;
                case "Cat":
                    if(part.equals("head"))
                        result = dogBreedHead;
                    else if(part.equals("torso"))
                        result = dogBreedTorso;
                    else
                        result = dogBreedLegs;
                    break;
                case "Rabbit":
                    result = calico;
                    break;
                case "Elephant":
                    result = rabbit;
                    break;
                case "Bear":
                    result = elephant;
                    break;
            } 
        }
        
        updateBreedScreen();
        return result;
        
    }
    
    // Takes price integer and returns string in dollar format
    private String formatPriceString(int price){
        String amt = Integer.toString(price);
        return "$" + amt + ".00";
    }
    
    // takes a price string and returns integer of equivalent value
    public int priceStringToInt(String price){
        String[] parts = price.split("\\.");
        return Integer.parseInt(parts[0].split("\\$", 2)[1]);
    }
    
